import Foundation

/// Represents different muscle groups that can be targeted in exercises.
public enum MuscleGroup {
    /// Exercises targeting the upper body muscles, including shoulders, chest, and arms.
    case upperBody

    /// Exercises targeting the lower body muscles, including legs and glutes.
    case lowerBody

    /// Exercises focusing on the core muscles, including abdominals and lower back.
    case core

    /// Exercises targeting the back muscles, including lats, traps, and rhomboids.
    case back

    /// Exercises focusing on the arm muscles, including biceps and triceps.
    case arms

    /// Exercises targeting the shoulder muscles, including deltoids.
    case shoulders

    /// Exercises focusing on the chest muscles, including pectorals.
    case chest

    /// Exercises targeting the leg muscles, including quadriceps, hamstrings, and calves.
    case legs
}
