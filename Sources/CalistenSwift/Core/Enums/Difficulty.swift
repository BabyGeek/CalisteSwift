    case beginner
    
        /// Exercises designed for individuals with some experience and basic strength.
    case intermediate
    
        /// Exercises that require a higher level of strength, skill, and experience.
    case advanced
    
        /// Exercises intended for highly experienced individuals with advanced skills and strength.
    case expert
}
